package egovframework.dnworks.cms.link.service;

import java.util.Date;

public class LinkClickVo 
{
	private Date click_dt;
	private int lnk_sn;
	private String click_agent;
	private String click_ip;
	private String click_locale;
	private String click_referer;
	public Date getClick_dt() {
		return click_dt;
	}
	public void setClick_dt(Date click_dt) {
		this.click_dt = click_dt;
	}
	public int getLnk_sn() {
		return lnk_sn;
	}
	public void setLnk_sn(int lnk_sn) {
		this.lnk_sn = lnk_sn;
	}
	public String getClick_agent() {
		return click_agent;
	}
	public void setClick_agent(String click_agent) {
		this.click_agent = click_agent;
	}
	public String getClick_ip() {
		return click_ip;
	}
	public void setClick_ip(String click_ip) {
		this.click_ip = click_ip;
	}
	public String getClick_locale() {
		return click_locale;
	}
	public void setClick_locale(String click_locale) {
		this.click_locale = click_locale;
	}
	public String getClick_referer() {
		return click_referer;
	}
	public void setClick_referer(String click_referer) {
		this.click_referer = click_referer;
	}
	
	
}
